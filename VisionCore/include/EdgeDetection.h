/**
 * @file    EdgeDetection.h
 * @brief   边缘检测与定位模块 —— 接口声明
 *
 * 本模块实现两个核心函数：
 *   1. detectEdgesByWavelet  —— 多尺度小波边缘检测
 *   2. locateGearNut         —— 齿轮/螺母连通域定位
 *
 * 算法依据立项报告第 3.1 节：
 *   - 多尺度小波变换模极大值法进行边缘检测
 *   - 二维离散小波变换：W(a,b) = ∫f(t)·Ψ*_{a,b}(t)dt
 *     其中 a 为尺度因子，b 为平移因子，Ψ 为小波基函数
 *   - 模极大值点对应图像边缘位置
 */

#pragma once

#include <opencv2/opencv.hpp>
#include <vector>

// ============================================================
// 多尺度小波边缘检测
//
// 原理：
//   采用墨西哥帽小波（Mexican Hat Wavelet）= 高斯函数的二阶导数
//   在频域中：Ψ̂(u,v) = (u²+v²)·σ²·exp(-(u²+v²)·σ²/2)
//   利用 OpenCV DFT 接口实现频域小波滤波：
//     Step1: 图像 DFT → 频谱
//     Step2: 频谱 × 各尺度小波滤波器
//     Step3: IDFT → 各尺度小波系数
//     Step4: 多尺度融合（等权叠加绝对值）
//     Step5: Otsu 阈值提取模极大值点集
//
// 参数：
//   src    —— 输入图像（CV_8UC3 或 CV_8UC1，内部自动转灰度）
//   scales —— 小波分解尺度数，默认 3（值越大检测粒度越粗）
//
// 返回值：
//   检测到的边缘点坐标集合（图像像素坐标系，原点在左上角）
// ============================================================
std::vector<cv::Point> detectEdgesByWavelet(
    const cv::Mat& src,
    int            scales = 3
);

// ============================================================
// 齿轮/螺母连通域定位
//
// 原理：
//   - 对二值图像调用 findContours 提取外部轮廓
//   - 以面积最大的连通域代表目标工件（工业场景假设）
//   - 调用 minAreaRect 计算最小外接矩形，包含：
//       .center —— 矩形中心坐标（目标定位结果）
//       .size   —— 矩形宽高（像素尺寸）
//       .angle  —— 旋转角度（与水平轴夹角，-90°~0°）
//
// 参数：
//   binary —— 输入二值图（CV_8UC1，前景 255，背景 0）
//
// 返回值：
//   cv::RotatedRect，若无有效轮廓则返回空 RotatedRect（center=0,0）
// ============================================================
cv::RotatedRect locateGearNut(const cv::Mat& binary);
