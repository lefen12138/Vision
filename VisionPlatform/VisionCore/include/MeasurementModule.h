/**
 * @file    MeasurementModule.h
 * @brief   尺寸测量模块 —— 接口声明
 *
 * 本模块实现齿轮公法线长度计算，
 * 严格遵照立项报告第 3.2 节公式 (2)：
 *
 *   W = m · cos(α) · [π(k - 0.5) + z · inv(α)]
 *
 *   其中：
 *     m     —— 模数（mm）
 *     α     —— 压力角（通常取标准值 20°）
 *     z     —— 总齿数
 *     k     —— 跨测齿数
 *     inv(α) —— 渐开线函数 = tan(α) - α（弧度制）
 *
 * 公法线长度是评估齿轮齿厚偏差的重要检测指标。
 * 实际测量时，公法线长度 W 即为跨 k 个齿时，
 * 与异侧齿廓相切的两平行平面间的距离。
 */

#pragma once

// ============================================================
// 渐开线函数
//
// 定义：inv(α) = tan(α) - α
// 物理意义：基圆切线展开角与切触角之差
//
// 参数：
//   pressureAngleDeg —— 压力角（度），标准值 20°
//
// 返回值：渐开线函数值（无量纲）
// ============================================================
double involuteFunction(double pressureAngleDeg);

// ============================================================
// 推荐跨测齿数
//
// 按《机械工程手册》标准公式：
//   k_recommend = floor(z · α / π + 0.5)
//
// 返回范围：[1, z/2]
//
// 参数：
//   toothCount       —— 总齿数 z
//   pressureAngleDeg —— 压力角（度），默认 20°
// ============================================================
int recommendSpanTeeth(int toothCount, double pressureAngleDeg = 20.0);

// ============================================================
// 齿轮公法线长度计算（核心函数）
//
// 公式（来源：立项报告公式 (2)）：
//   W = m · cos(α) · [π(k - 0.5) + z · inv(α)]
//
// 算法流程：
//   1. 验证输入参数有效性
//   2. 若 spanTeeth <= 0，调用 recommendSpanTeeth 自动计算
//   3. 计算渐开线函数 inv(α) = tan(α) - α
//   4. 代入公式计算公法线长度 W
//
// 参数：
//   toothCount —— 总齿数 z（正整数）
//   moduleMM   —— 模数 m（毫米，正数）
//   spanTeeth  —— 跨测齿数 k（<=0 时自动推荐）
//
// 返回值：
//   公法线长度 W（毫米），参数无效时返回 0.0
// ============================================================
double calculateGearNormalLength(
    int    toothCount,
    double moduleMM,
    int    spanTeeth = 2
);
